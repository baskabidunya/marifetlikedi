export interface FunTestOption {
  text: string;
  score: number;
}

export interface FunTestQuestion {
  text: string;
  options: FunTestOption[];
}

export interface FunTestResult {
  title: string;
  description: string;
  advice: string;
  scoreRange: [number, number];
}

export function computeMaxScore(test: FunTest): number {
  return test.maxScore ?? test.questions.reduce(
    (sum, q) => sum + Math.max(...q.options.map(o => o.score)),
    0
  );
}

export interface FunTest {
  id: string;
  title: string;
  description: string;
  icon: string;
  questions: FunTestQuestion[];
  results: FunTestResult[];
  maxScore?: number;
}

function t(text: string, options: [string, number][]): FunTestQuestion {
  return {
    text,
    options: options.map(([text, score]) => ({ text, score })),
  };
}

